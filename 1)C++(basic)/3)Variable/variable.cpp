#include <iostream>
using namespace std;

int main() {
    int age = 30;          // Integer variable
    float salary = 56000.50; // Floating-point variable
    double pi = 3.1415926535; // Double precision variable
    char grade = 'B';      // Character variable

    cout << "Age: " << age << endl;
    cout << "Salary: " << salary << endl;
    cout << "Value of Pi: " << pi << endl;
    cout << "Grade: " << grade << endl;

    return 0;
}
